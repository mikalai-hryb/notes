import matplotlib.pyplot as plt
import matplotlib.patches as mpatches

# Создаем "лестницу целей" в виде схемы
fig, ax = plt.subplots(figsize=(10, 6))

# Уровни целей
levels = {
    "Долгосрочные цели (10 лет)": [
        "Дом и семья",
        "Доход 5000$+ (неск. источников)",
        "Lead/CTO или предприниматель",
        "Физическая форма",
        "Английский, польский, беларуск. — свободно",
        "Пианино и единоборство",
        "Проект о беларускости",
    ],
    "Среднесрочные цели (1–3 года)": [
        "Senior/Lead DevOps",
        "Доход 3000–3500$",
        "Инвестиции (ETF, недвижимость)",
        "Английский B2/C1, польский B1/B2",
        "Уроки фортепиано",
        "Регулярные тренировки, начать единоборство",
        "Новые друзья и партнёр",
        "Запуск MVP проекта",
    ],
    "Краткосрочные цели (3–6 мес.)": [
        "Курс по архитектуре/лидерству",
        "Обновить резюме, мониторить вакансии",
        "Начать инвестировать (100–200$/мес)",
        "Английский с преподавателем, польский на курсах",
        "Спортплощадка 3 раза в неделю",
        "Пробное занятие по единоборству",
        "Больше встреч с друзьями",
        "Прописать идею проекта",
    ],
}

# Настройки координат
y_positions = [3, 2, 1]  # три уровня
colors = ["#6a5acd", "#4682b4", "#3cb371"]

for i, (level, goals) in enumerate(levels.items()):
    y = y_positions[i]
    ax.text(-0.05, y, level, ha="right", va="center", fontsize=12, fontweight="bold")

    for j, goal in enumerate(goals):
        ax.text(
            j * 1.5,
            y,
            f"• {goal}",
            fontsize=10,
            va="center",
            ha="left",
            color=colors[i],
        )

# Убираем оси
ax.axis("off")

# Заголовок
plt.title(
    "Лестница целей: краткосрочные → среднесрочные → долгосрочные",
    fontsize=14,
    fontweight="bold",
)
plt.tight_layout()
plt.show()
